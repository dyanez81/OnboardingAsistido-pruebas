import Geolocation from '@react-native-community/geolocation';
import {hasLocationPermission} from './permissions';

/**
 * @Desc:
 * @date 2021-05-26 13:35:26
 * @returns {Promise<Object>} Coordenadas GPS =>
 * {coords: {accuracy, altitude, heading, latitude, longitude, speed}, mocked, timestamp}
 */
export const getGpsLocation = async () => {
  const _permission = await hasLocationPermission();

  return new Promise((resolve, reject) => {
    if (_permission.result === false) reject(_permission.message);
    else {
      Geolocation.getCurrentPosition(
        position => {
          resolve(position);
        },
        error => {
          reject(error);
        },
        {
          timeout: 30000,
          maximumAge: 10000,
          distanceFilter: 300,
        },
      );
    }
  });
};

/**
 * Indica si las coordenadas son parte de la república mexicana
 * @param lat number latitude
 * @param lng number longitude
 */
export const isMexico = (lat, lng) => {
  if (typeof lat !== 'number' || typeof lng !== 'number') {
    return new Error('Coordenadas incorrectas (' + lat + ',' + lng + ')');
  }

  let found = false;
  let point = [lng, lat];
  let i = 0;
  do {
    found = pointInPolygon(mexicoPolygon[i], point);
    i++;
  } while (i < mexicoPolygon.length && !found);

  return found;
};

/**
 * Checks if a point is contained in a polygon
 * (based on the Jordan curve theorem), for more info:
 * http://www.ecse.rpi.edu/Homepages/wrf/Research/Short_Notes/pnpoly.html
 * @param polygon array a series of the polygon's coordinates
 * @param point object representing the point's coordinates
 * @return boolean true if the point lies within the polygon, false otherwise
 */
const pointInPolygon = (polygon, point) => {
  var nvert = polygon.length;
  var c = false;
  var i = 0;
  var j = 0;
  for (i = 0, j = nvert - 1; i < nvert; j = i++) {
    if (
      polygon[i][1] > point[1] != polygon[j][1] > point[1] &&
      point[0] <
        ((polygon[j][0] - polygon[i][0]) * (point[1] - polygon[i][1])) /
          (polygon[j][1] - polygon[i][1]) +
          polygon[i][0]
    ) {
      c = !c;
    }
  }
  return c;
};

