/**
 * @Desc: Procesa la respuesta de la API de GeoCode de Google
 * @date 2021-05-19 18:56:01
 */
export const processGoogleAddress = data => {
  let _r = {
    cp: '',
    country: '',
    federalEntity: '',
    city: '',
    municipality: '',
    suburb: '',
    street: '',
    externalNumber: '',
    neighborhoods: [],
  };

  const _results = data.results;

  let _result;
  if (isType(_results, 'street_address')) {
    _result = _results.filter(r => r.types[0] === 'street_address')[0];
  } else if (isType(_results, 'premise')) {
    _result = _results.filter(r => r.types[0] === 'premise')[0];
  } else if (isType(_results, 'route')) {
    _result = _results.filter(r => r.types[0] === 'route')[0];
  } else {
    _result = _results[0];
  }

  const _components = _result.address_components;

  const _cp = _components.filter(item => item.types[0] === 'postal_code')[0];
  if (_cp) _r.cp = _cp.long_name;

  const _c = _components.filter(item => item.types[0] === 'country')[0];
  if (_c) _r.country = _c.long_name;

  const _s = _components.filter(
    item => item.types[0] === 'administrative_area_level_1',
  )[0];
  if (_s) _r.federalEntity = _s.long_name;

  const _ci = _components.filter(item => item.types[0] === 'locality')[0];
  if (_ci) _r.city = _ci.long_name;

  // Exclusivamente para alcaldía / municipio
  if (isType(_results, 'administrative_area_level_3')) {
    const _compDel = _results.filter(
      r => r.types[0] === 'administrative_area_level_3',
    )[0].address_components;

    const _de = _compDel.filter(
      item => item.types[0] === 'administrative_area_level_3',
    )[0];

    if (_de) _r.municipality = _de.long_name;
  } else if (isType(_results, 'administrative_area_level_2')) {
    const _compMun = _results.filter(
      r => r.types[0] === 'administrative_area_level_2',
    )[0].address_components;

    const _m = _compMun.filter(
      item => item.types[0] === 'administrative_area_level_2',
    )[0];

    if (_m) _r.municipality = _m.long_name.replace(' Municipality', '');
  } else if (_ci) {
    _r.municipality = _ci.long_name;
  }

  const _su = _components.filter(item => item.types[0] === 'political')[0];
  if (_su) _r.suburb = _su.long_name;

  const _st = _components.filter(item => item.types[0] === 'route')[0];
  if (_st) _r.street = _st.long_name;

  const _n = _components.filter(item => item.types[0] === 'street_number')[0];
  if (_n) _r.externalNumber = _n.long_name;

  _r.neighborhoods = _result.postcode_localities;

  return _r;
};

/**
 * @Desc: Verifica si ecisten resultados con un Type específico
 * @date 2021-05-19 17:31:15
 */
const isType = (items, search) => {
  if (!items || items.length === 0) return false;

  return items.filter(item => item.types[0] === search).length > 0;
};
